'''
Using the previously created variables first_name and last_name, perform the concatenation of these variables to form the full name.

Tasks:
Using the variables first_name and last_name, create a new variable full_name.
The full_name variable should be the result of concatenating (first_name, a space " ", and last_name).
Use the + operator to concatenate the strings.
Expected result:
The program should create and output the full_name variable, which contains the full name composed of the first name and last name, separated by a space.

Tips:
Make sure there is a space between the first name and last name.
Remember that the + operator is used to concatenate strings.
'''

# Declare variables for first name and last name
first_name = "Viktoriia"
last_name = "Kratser"

# Concatenate first name and last name with a space in between to form full name
full_name = first_name + " " + last_name

# Print the full name
print("Full Name:", full_name)
